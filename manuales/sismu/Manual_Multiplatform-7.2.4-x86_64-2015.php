<h2><span class="mw-headline" id="Graphics_Cards">Graphics Cards</span></h2>
<p>When using USB or network zero client devices, only one video card is used per system. This is called the "onboard" video card and may either be integrated into the motherboard or added via PCI expansion slot. 
</p><p>For onboard video only, Userful Multiplatform&#8482; is compatible with any video card supported by the underlying Linux distribution. <i>(<b>Note:</b> If using <a href="/Manuals/Video_Card_Zero_Clients#Choosing_Video_Cards" title="Manuals/Video Card Zero Clients" class="mw-redirect"> video card zero clients</a> for Multiplatform, the onboard video card must either be nVIDIA or be disabled.)</i>
</p>
<table border="1" cellpadding="4" cellspacing="0" style="width:50%; padding:.5em 1em; background:#E3f6ce; border:#D8D8D8;">


<tr>
<td>
<p>Information on video cards supported by the underlying Linux distribution can be difficult to find; the most reliable sources of information are the README files or Linux "man pages" for the driver that will be used in the system.  
</p>
<ul>
<li> <b>If considering an nVIDIA graphics card for onboard video</b>, bear in mind that the open source drivers <code>nv</code> and <code>nouveau</code> are not compatible with Userful Multiplatform&#8482;. To get information on the proprietary nVIDIA drivers for Linux, go to  <a target="_blank" class="external free" href="http://www.nvidia.com/Download/index.aspx?lang=en-us">http://www.nvidia.com/Download/index.aspx?lang=en-us</a> and search for the video card or driver you wish to use. 
</li>
</ul>
<ul>
<li> <b>If considering an ATI/Radeon graphics card for onboard video</b>, it is best to look at the driver "manual page" or "man page" at <a target="_blank" class="external free" href="http://linux.die.net/man/4/radeon">http://linux.die.net/man/4/radeon</a>
</li>
</ul>
<ul>
<li> <b>If considering an Intel graphics card for onboard video</b>, you can find the driver man page at <a target="_blank" class="external free" href="http://linux.die.net/man/4/intel">http://linux.die.net/man/4/intel</a>
</li>
</ul>
</td></tr></table>
<p>It is important to remember that Linux operating systems often don't support "bleeding edge" graphics cards, so it's worthwhile to research the options carefully, or contact your Userful representative for advice. 
</p>
<table border="0" cellpadding="4" cellspacing="0" style="width:100%; padding:.5em 1em; background:#ffdddd; border:none;">

<tr>
<td>
<p><b>Please Note: Only one video output head of motherboards with dual-head radeon graphics is supported. </b>
</p><p>If the host PC has a motherboard with integrated Radeon graphics, and also has two video output heads, only one output head will be usable with Userful Multiplatform&#8482; enabled. The system will crash after reboot until a system administrator manually edits the <code>/etc/userful/extra_devices.db</code> to select either the VGA (D-sub) or DVI output head.  To do so:
</p>
<ol>
<li> Find the graphic card model used in the host PC.
</li>
<li> Start the computer in single-user mode (with Userful Multiplatform&#8482; disabled). Alternatively, if Multiplatform is enabled and the system has been started, wait three minutes and Multiplatform will automatically turn off, returning the system to single-user mode. 
</li>
<li> Open a terminal and either log in as root or use <code>sudo</code> throughout this procedure. 
</li>
<li> Open the <code>/etc/userful/extra_devices.db</code> file in a text editor. 
</li>
<li> Find the <code># VGA devices</code> list within that file and update it so that only one port is listed, either 'DVI' or 'D-sub' (VGA), after <code>MainPorts</code>.
</li>
</ol>
<dl>
<dd> Example: The original entry...
</dd>
</dl>
<pre>('video', 0x1002, 0x5b63, any, any): dict(
       Model = 'Radeon X550 (0xPCIE)',  MainPorts =  ['DVI', 'D-sub']),</pre>
<dl>
<dd> Should be changed to (for DVI output):
</dd>
</dl>
<pre>('video', 0x1002, 0x5b63, any, any): dict(
       Model = 'Radeon X550 (0xPCIE)',  MainPorts =  ['DVI']),</pre>
<dl>
<dd> Should be changed to (for VGA output):
</dd>
</dl>
<pre>('video', 0x1002, 0x5b63, any, any): dict(
       Model = 'Radeon X550 (0xPCIE)',  MainPorts =  ['D-sub']),</pre>
</td></tr></table>
