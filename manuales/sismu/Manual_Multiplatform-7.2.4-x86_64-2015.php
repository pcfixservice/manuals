<h2><span class="mw-headline" id="Introduction">Introduction</span></h2>
<p>Userful Multiplatform™ is a complete operating system, based on CentOS (see <a target="_blank" class="external free" href="http://www.centos.org">http://www.centos.org</a> for details) that include software that extends Linux to support several fully independent and concurrent workstations using a single computer box. This is accomplished by adding video devices (either  network zero client devices, USB zero client devices, video card zero clients or, keyboards and mice to a single host PC and installing Userful Multiplatform™.
</p>
<h2><span class="mw-headline" id="Features">Features</span></h2>
<p><b>Userful MultiPlatform™</b> provides:
</p>
<ul>
<li> A stable, versatile base operating sytem (CentOS). 
</li>
<li> Full multimedia support 
</li>
<li> Multiseat capability based on video cards, USB zero client devices or network zero client devices. 
</li>
<li> Improved system and station management user interfaces. 
</li>
<li> Enhanced system stability.
</li>
<li> Install DVD (64bit OS), Browser, office suite, productivity tools, and one-click easy install of thousands of free software applications for a range of business, educational and other uses. 
</li>
<li> Local Desktops: Win7, Win8, Linux, Cloud (Chrome browser desktop) 
</li>
<li> Session Launcher 
</li>
<li> Media playback
</li>
<li> A unified browser-accessible control center is provided that provides full administration capabilities including: 
<ul>
<li> Station Mapping 
</li>
<li> Assign / release network stations 
</li>
<li> Enable / Disable / rename stations 
</li>
<li> Set monitor resolution and color depth (individual & system wide) 
</li>
<li> Display Power Management Signalling (DPMS)
</li>
<li> Integrated help & support
</li>
<li> Remote access 
</li>
<li> System Updates 
</li>
<li> Shared Storage Management
</li>
<li> Virtual Desktop Infrastructure support, with simple and intuitive management of virtual machines (including VM import)
</li>
<li> Change keyboard layout
</li>
<li> System Diagnostics utility for troubleshooting 
</li>
</ul>
</li>
</ul>





