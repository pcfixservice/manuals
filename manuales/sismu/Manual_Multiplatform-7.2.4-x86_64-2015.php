<h3><span class="mw-headline" id="DisplayPort">DisplayPort</span></h3>
<p>Some motherboards and/or video cards have <a target="_blank" class="external text" href="http://wikipedia.org/wiki/DisplayPort">DisplayPort</a> connections instead of, or in addition to, VGA or DVI connections. 
</p><p>These devices can work with Userful Multiplatform&#8482;, but require manual configuration to add the device to the database. 
</p><p><b>To add a DisplayPort video device to the Userful device database:</b>
</p>
<ol>
<li> Find the graphic card model used in the host PC. To add the DisplayPort or Mini DisplayPort, the entry for this graphic card will be edited. 
</li>
<li> Start the computer in single-user mode (with Userful Multiplatform disabled). 
</li>
<li> Open a terminal and either log in as root or use <code>sudo</code> throughout this procedure. 
</li>
<li> Open the <code>/etc/userful/extra_devices.db</code> file in a text editor. 
</li>
<li> Find the <code># VGA devices</code> list within that file. Find the video card used in the system and add either 'Display Port' or 'Mini Display Port' to the appropriate sub-section as follows:
</li>
</ol>
<pre> ('video', 0x10de, 0x0659, any, any): dict(
      Model = '&lt;VIDEO CARD&gt;', MainPorts =  ['DVI', 'D-sub', 'Mini Display Port']),</pre>
<dl>
<dd> Example: For an nVIDIA Quadro FX 580 card, add the following line to the <code># NVIDIA</code> sub-section of the <code># VGA devices</code> section:
</dd>
</dl>
<pre> 'video', 0x10de, 0x0659, any, any): dict(
      Model = 'Quadro FX 580', MainPorts =  ['DVI', 'D-sub', 'Mini Display Port']),</pre>
