<font color="red">Hello again. In this video, we are going to discuss three types of questions which we can use to make our language less direct.</font>
Let's begin then.
The most common way to ask a polite question is to make your question indirect.
Let’s look at a few examples.
For instance, you need to urge a colleague to finish a task. You can ask <font color="blue">‘What do you need to get this task done?'</font>
But if we ask an indirect questions, it becomes a lot more polite.
For example, <font color="blue">'Could you tell me what you need to get this task done?’.</font>

Another example – imagine you arrive late for a meeting and, to make things worse, 
you don’t know where it is taking place, so you can ask a very direct question:' 
<font color="blue">Where is the meeting room?',</font> which is fine, but could be considered a little rude.
We can soften it by using an introductory phrase: <font color="blue">'Do you happen to know where the meeting room is?'</font>

There are a few introductory phrases for building indirect questions, they are
<font color="blue">Could you tell me…, Do you happen to know…, Do you know…, I was wondering/I wonder, Do you have any idea…, May I ask.</font>
Please keep in mind, however, that in order to make an indirect question you need to follow certain grammar rules.
For more details you can visit the section GRAMMAR NOTE in this lesson.
Overall, indirect questions increase the level of politeness and, therefore, they are particularly useful when we ask somebody we don't know very well.

<font color="red">Another type of polite questions are negative questions.</font>
Imagine that you have some concerns about implementing a risky solution. You could say:
<font color="blue">We will be taking a huge risk.</font>
This message offers only one point of view, your opinion. If, however, we turn it into a negative question:
<font color="blue">Wouldn’t we be taking a huge risk?,</font> we also invite the listener to take part in the decision-making process.

One more example:
<font color="blue">It is better to discuss this proposal with other team members.</font>
In this sentence we express our opinion quite directly about the need to discuss the proposal with other colleagues.
By turning the sentence into a negative question, <font color="blue">Wouldn’t it be better to discuss this proposal with other team members?</font>
we not only make our opinion sound less direct, but also encourage the listeners to agree with it.
So, you can form negative questions using <font color="blue">shouldn’t, wouldn’t, don’t, won’t, etc.</font>
The major benefit of such questions is that they leave some room for discussion and can help you be more persuasive, which can be extremely useful in negotiations.

<font color="blue">The last type of questions we are going to discuss are question tags.</font>
Question tags can help us sound more polite in a few ways. Let’s take a look:
<font color="blue">These new sales figures are promising.</font>
This sentence on its own is just an announcement.
It doesn’t encourage any form of response. We can invite the listener to agree with us by adding a tag, for example
<font color="blue">These new sales figures are promising, aren’t they?</font>
Question tags can be particularly helpful during presentations by keeping the audience involved.

Tags come in handy also for softening our suggestions. For example:
<font color="blue">Let’s start the meeting.</font> By adding a tag, we make it more polite: <font color="blue">Let’s start the meeting, shall we?</font>
To sum up, question tags can be used to encourage interaction and make suggestions softer.
In order to build question tags you need to follow certain grammar rules.
For more details you can visit the section GRAMMAR NOTE in this lesson.
