<font color="red">In this lesson, we are going to examine the ways of saying NO politely</font>
first in a less formal situation when making 'small talk' with a colleague, and then 'in meetings' when talking to your customer or manager.
We hope in this lesson you will learn how to soften the ‘NO’ answer and minimize the discomfort caused by saying NO.

Let’s start with a less formal situation when you need to say NO to a colleague.
You are going to hear two conversations in the kitchen area in an EPAM office. 
In which conversation does the speaker sound more polite when saying no?
Undoubtedly, 'Conversation 2' is much more successful.

Instead of saying a point-blank NO, as in the first conversation, the speaker uses such alternatives as <font color="blue">'Not really'; 'Not exactly'.</font>
These alternatives should be used if you don’t know someone very well or simply want to soften the effect of a negative answer.
Saying just NO sounds harsh and blocks communication.

So, let’s look at some more examples of similar expressions
<font color="blue">'Not especially', 'Not yet', 'Not any more', 'Not at the moment', 'Not as far as I know'</font>
We can also add one of the following phrases to soften the negative message:
<font color="blue">'Actually, not yet', 'As a matter of fact, not any more'.</font>
Using this type of language opens the opportunity for a more successful and meaningful exchange and is much more polite than just a blunt NO.
I hope you’ve found this information useful. Now, please move on to the exercises in this section to practice 
using alternatives to saying NO presented in this video.

